package Patterns;

public class solidRct {
    public static void main(String args[]){
        int n=4;//row
        int m=5;//column

        
        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++){
                System.out.print(" * ");
            }
            System.out.println();
        }
    }
    
}
